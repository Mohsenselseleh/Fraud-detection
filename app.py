# app.py
import streamlit as st
import pandas as pd
import numpy as np
from sklearn.metrics import confusion_matrix
import joblib

# Load model and data
xgb = joblib.load("xgb_model.pkl")
X_test = pd.read_csv("X_test.csv")
y_test = pd.read_csv("y_test.csv").values.ravel()

# Get prediction probabilities
probs = xgb.predict_proba(X_test)[:, 1]

# Cost function
def fraud_cost(tp, fp, fn, tn):
    return tp*100 + fp*(-10) + fn*(-200)

# Streamlit UI
st.title("ğŸ’° Fraud Detection Threshold Simulator")

threshold = st.slider("Select threshold", 0.0, 1.0, 0.5)
preds = (probs >= threshold).astype(int)
tn, fp, fn, tp = confusion_matrix(y_test, preds).ravel()

cost = fraud_cost(tp, fp, fn, tn)

st.metric("ğŸ“Š Total Cost", f"${cost}")
st.write("âœ… True Positives:", tp)
st.write("âŒ False Positives:", fp)
st.write("ğŸ§¨ False Negatives:", fn)
st.write("ğŸŸ¢ True Negatives:", tn)